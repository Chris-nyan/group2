    private String Capital_City;

    /**
            * Country continent
     */
    private String Country_Name;

    /**
            * Country continent
     */
    private int Population;

    /**
            * Country continent
     */
    private String Continent;

    public String getCapital_City() {
        return Capital_City;
    }

